<?php
/**
 * Шаблоны тем и текстов уведомлений по событиям срока годности.
 */
declare(strict_types=1);

function expiryNotificationSubject(int $daysLeft): string
{
    return match ($daysLeft) {
        180 => 'Срок годности партии истекает через 180 дней.',
        90 => 'Срок годности партии истекает через 90 дней.',
        60 => 'Срок годности партии истекает через 60 дней.',
        30 => 'Срок годности партии истекает через 30 дней.',
        15 => 'Срок годности партии истекает через 15 дней.',
        1 => 'Срок годности партии истекает через 1 день.',
        0 => 'Истек срок годности партии товаров.',
        default => 'Срок годности партии истекает через ' . $daysLeft . ' дней.',
    };
}

function expiryNotificationBody(array $batches, int $daysLeft): string
{
    $lines = [];
    foreach ($batches as $batch) {
        $lines[] = sprintf(
            'Артикул %s Срок годности до %s',
            (string)($batch['article'] ?? ''),
            formatExpiryMonth((string)($batch['expiry_date'] ?? ''), (bool)($batch['expiry_full_date'] ?? false))
        );
    }

    $lines[] = '';
    $lines[] = expiryNotificationInstruction($daysLeft);

    return implode("\n", $lines);
}

