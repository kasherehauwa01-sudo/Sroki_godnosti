}

function formatExpiryMonth(string $expiryDate, bool $forceFull = false): string
{
    $timestamp = strtotime($expiryDate);
    if (!$timestamp) {
        return $expiryDate;
    }

    return $forceFull || date('d', $timestamp) !== '01'
        ? date('d.m.Y', $timestamp)
        : date('m.Y', $timestamp);
}
